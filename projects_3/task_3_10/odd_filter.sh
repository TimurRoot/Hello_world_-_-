#!/bin/bash

echo "Нечётные числа от 1 до 20 (остановка на 15):"

for i in {1..20}; do
    # Останавливаем цикл, если встретили 15
    if [ $i -eq 15 ]; then
        echo "Встречено число 15 — остановка работы."
        break
    fi

    # Пропускаем чётные числа
    if [ $((i % 2)) -eq 0 ]; then
        continue
    fi

    # Выводим нечётное число
    echo "$i"
done
