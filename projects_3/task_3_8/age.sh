#!/bin/bash

# Калькулятор возраста
# Устанавливаем год рождения как неизменяемую переменную
readonly BIRTH_YEAR=1990


# Получаем текущий год (из системной даты)
CURRENT_YEAR=$(date +%Y)

# Вычисляем возраст: текущий год минус год рождения
AGE=$((CURRENT_YEAR - BIRTH_YEAR))

# Выводим результат
echo "Год рождения: $BIRTH_YEAR"
echo "Текущий год: $CURRENT_YEAR"
echo "Ваш возраст: $AGE лет"
