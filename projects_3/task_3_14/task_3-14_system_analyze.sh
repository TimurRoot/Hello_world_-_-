#!/bin/bash

echo "=== Анализ использования дискового пространства ==="
echo "Файловая система | Процент заполнения"
echo "----------------------------------"

# Анализируем вывод df -h с помощью awk
df -h | awk '
NR > 1 {
    # Сохраняем название файловой системы (1‑й столбец) и процент заполнения (5‑й столбец)
    filesystem = $1
    percent = $5

    # Удаляем символ % из процента для числового сравнения
    gsub(/%/, "", percent)

    # Выводим название ФС и процент
    printf "%-20s %s\n", filesystem, $5

    # Если процент > 90, выводим предупреждение
    if (percent > 90) {
        printf "ВНИМАНИЕ: %s заполнена на %s! Требуется внимание!\n", filesystem, $5
    }
}
'
