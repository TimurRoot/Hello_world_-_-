# Программа для расчёта индекса массы тела (ИМТ)

# 1. Запрашиваем у пользователя данные
weight = float(input("Введите ваш вес (кг): "))
height_cm = float(input("Введите ваш рост (см): "))

# 2. Переводим рост из сантиметров в метры (для формулы ИМТ)
height_m = height_cm / 100

# 3. Рассчитываем индекс массы тела по формуле: ИМТ = вес / (рост в метрах)²
bmi = weight / (height_m ** 2)

# 4. Выводим отчёт с использованием табуляции (\t) для отступов
print("\n--- Отчет о состоянии здоровья ---")
print(f"Рост:\t\t{height_cm:.1f} см")
print(f"Вес:\t\t{weight:.1f} кг")
print(f"Индекс массы тела:\t{bmi:.2f}")
