donor_blood_group = input("Введите группу крови донора (I, II, III, IV или 0): ").upper()
recipient_blood_group = input("Введите группу крови реципиента (пациента) (I, II, III, IV или 0): ").upper()

if donor_blood_group == recipient_blood_group:
    print("Переливание возможно — группа крови донора соответствует группе крови реципиента.")
elif donor_blood_group == "0":
    print("Переливание возможно — группа крови донора (0) позволяет переливать её любому реципиенту.")
else:
    print("Переливание невозможно — группа крови донора не соответствует группе крови реципиента и не является универсальной (0).")
