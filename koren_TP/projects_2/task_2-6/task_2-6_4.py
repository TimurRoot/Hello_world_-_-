def choose_path():
    print("=== Витязь на распутье ===")
    print("Перед вами три пути:")
    print("1. Путь в тёмный лес")

    print("2. Путь через топкое болото")
    print("3. Путь через горы")

    choice = input("Выберите путь (1/2/3): ")

    if choice == '1':
        print("\nВы выбрали путь в тёмный лес...")
        if input("Готовы ли вы сразиться с чудовищами? (да/нет): ").lower() == 'да':
            print("Вы победили всех врагов и нашли сокровища!")
        else:
            print("Вы вернулись назад, сохранив жизнь.")

    elif choice == '2':
        print("\nВы выбрали путь через болото...")
        if input("Хотите рискнуть и перейти болото? (да/нет): ").lower() == 'да':
            if input("У вас есть болотные сапоги? (да/нет): ").lower() == 'да':
                print("Вы успешно перешли болото и нашли целебные травы!")
            else:
                print("Вы увязли в болоте...")
        else:
            print("Вы решили найти другой путь.")

    elif choice == '3':
        print("\nВы выбрали путь через горы...")
        if input("Готовы ли вы к опасностям? (да/нет): ").lower() == 'да':
            if input("У вас есть опыт скалолазания? (да/нет): ").lower() == 'да' or \
                    input("У вас есть проводник? (да/нет): ").lower() == 'да':
                print("Вы покорили горы и нашли древний храм!")
            else:
                print("Путь оказался слишком сложным, вы вернулись.")
        else:
            print("Вы решили не рисковать.")

    else:
        print("\nНеверный выбор пути! Попробуйте снова.")


if __name__ == "__main__":
    choose_path()
