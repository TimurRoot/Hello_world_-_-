# Ввод числа N
N = int(input("Введите N: "))

# Начальная инициализация суммы
S = 0

# Инициализация счётчика
i = 1

# Цикл: пока i <= N
while i <= N:
    square = i * i   # или i ** 2
    S = S + square   # S += square
    i = i + 1        # i += 1

# Вывод результата
print(f"Сумма квадратов чисел от 1 до {N} = {S}")