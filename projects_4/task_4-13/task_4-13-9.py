# Ввод размера массива
N = int(input("Введите размер массива N: "))

# Проверка на пустой массив
if N < 1:
    print("Ошибка: массив пуст")
else:
    # Ввод элементов массива
    an = []
    for j in range(N):
        element = int(input(f"Введите an[{j + 1}]: "))
        an.append(element)

    # Начальная инициализация суммы
    Sum = 0

    # Инициализация счётчика
    i = 1

    # Цикл для обработки всех элементов
    while i <= N:
        # Проверка: является ли индекс нечётным? (в блок-схеме: i % 2 != 0)
        if i % 2 != 0:  # нечётный индекс
            Sum = Sum + an[i - 1]  # an[i-1] так как в Python индексация с 0
        i = i + 1

    # Вывод результата
    print("Сумма элементов с нечётными индексами:", Sum)