# Ввод четырёх чисел
A = float(input("Введите число A: "))
B = float(input("Введите число B: "))
C = float(input("Введите число C: "))
D = float(input("Введите число D: "))

# Вычисление суммы
S = A + B + C + D

# Вычисление среднего арифметического
I = S / 4

# Вывод результата
print("Среднее арифметическое:", I)