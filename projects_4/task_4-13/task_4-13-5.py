# Ввод количества чисел
N = int(input("Введите количество чисел N: "))

# Ввод первого числа (начальное значение максимума)
max_val = float(input("Введите число 1: "))

# Инициализация счётчика
i = 2

# Цикл для оставшихся N-1 чисел
while i <= N:
    current = float(input(f"Введите число {i}: "))

    if current > max_val:
        max_val = current

    i = i + 1

# Вывод результата
print("Максимальное число:", max_val)