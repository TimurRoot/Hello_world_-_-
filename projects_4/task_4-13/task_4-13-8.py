# Ввод размера массива
N = int(input("Введите размер массива N: "))

# Ввод элементов массива A[1..N]
A = []
for j in range(N):
    element = float(input(f"Введите A[{j + 1}]: "))
    A.append(element)

# Начальная инициализация счётчика
count = 0

# Инициализация счётчика цикла
i = 1

# Цикл для проверки всех элементов
while i <= N:
    if A[i - 1] > 0:  # проверка, является ли элемент положительным
        count = count + 1  # count += 1
    i = i + 1

# Вывод результата
print("Количество положительных чисел в массиве:", count)