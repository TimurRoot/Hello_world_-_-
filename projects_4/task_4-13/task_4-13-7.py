# Ввод размера массива
N = int(input("Введите размер массива N: "))

# Ввод элементов массива A[1..N]
A = []
for j in range(N):
    element = float(input(f"Введите A[{j + 1}]: "))
    A.append(element)

# Начальная инициализация суммы
sum_val = 0

# Инициализация счётчика
i = 1

# Цикл для суммирования всех элементов (пока i <= N)
while i <= N:
    sum_val = sum_val + A[i - 1]  # A[i-1] так как в Python индексация с 0
    i = i + 1

# Вычисление среднего арифметического
average = sum_val / N

# Вывод результата
print("Среднее арифметическое массива:", average)